-- Função para obter a missão correspondente ao nível atual do jogador
local function getMissionByLevel(MyLevel)
    for _, mission in ipairs(World2Missions) do
        if MyLevel >= mission.minLevel and MyLevel <= mission.maxLevel then
            return mission
        end
    end
    return nil -- Caso não haja missão definida para o nível
end

